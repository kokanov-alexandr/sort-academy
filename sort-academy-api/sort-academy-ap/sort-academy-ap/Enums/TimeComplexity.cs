namespace sort_academy_api.Enums;

/// <summary>
/// Временная сложность
/// </summary>
public enum TimeComplexity
{
    /// <summary>
    /// O(1)
    /// </summary>
    O_1 = 1,

    /// <summary>
    /// O(log n)
    /// </summary>
    O_log_n = 2,

    /// <summary>
    /// O(√n)
    /// </summary>
    O_sqrt_n = 3,

    /// <summary>
    /// O(n)
    /// </summary>
    O_n = 4,

    /// <summary>
    /// O(n log n)
    /// </summary>
    O_n_log_n = 5,

    /// <summary>
    /// O(n^2)
    /// </summary>
    O_n_squared = 6,

    /// <summary>
    /// O(n^3)
    /// </summary>
    O_n_cubed = 7,

    /// <summary>
    /// O(2^n)
    /// </summary>
    O_2_to_the_n = 8,

    /// <summary>
    /// O(n!)
    /// </summary>
    O_n_factorial = 9
}


